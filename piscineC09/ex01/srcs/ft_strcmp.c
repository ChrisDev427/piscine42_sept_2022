#include "ft.h"

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while ((*s1 != '\0') && (*s1 == *s2))
	{
		s1++;
		s2++;
	}
	if (*s1 - *s2 > 0)
		return (*s1 - *s2);
	else if (*s1 - *s2 < 0)
		return (*s1 - *s2);
	else
	   	return (0);
}
